int diva(int x,int y)
{
  if(y==0){
     printf("yが0\n"); 
     return -1; 
  }
  else if(x==0){
    return 0;
  }
  return ((x-y+(N-1))%(N-1))+1;
}

int inv(int l){


  return diva(1,l);
}


